using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;

public class PlayerController : MonoBehaviour
{
    [SerializeField]
    private float dragDistance = 10f; // 드래그 거리
    private Vector3 touchStart;         // 터치 시작 위치
    private Vector3 touchEnd;           // 터치 종료 위치

    private Movement movement;

    private ChanceGauge chanceGauge;
    Animator animator;

    private void Awake()
    {
        movement = GetComponent<Movement>();
        chanceGauge = GameObject.Find("ChancePanel").GetComponent<ChanceGauge>();
    }

    private void Update()
    {
        if (Application.isMobilePlatform)
        {
            OnMobilePlatform();     // 모바일인가
        }
        else
        {
            OnPCPlatform();     //PC인가 확인
        }

    }

    private void OnMobilePlatform()
    {
        // 현재 화면을 터치하고 있지 않으면 메소드 종료
        if (Input.touchCount == 0) return;

        // 첫 번째 터치 정보 불러오기
        Touch touch = Input.GetTouch(0);

        // 터치 시작
        if(touch.phase == TouchPhase.Began)
        {
            touchStart = touch.position;
        }

        // 터치 & 드래그
        else if (touch.phase == TouchPhase.Moved)
        {
            touchEnd = touch.position;

            OnDragXY();
        }
    }

    private void OnPCPlatform()
    {
        // 터치 시작
        if (Input.GetMouseButtonDown(0))
        {
            touchStart = Input.mousePosition;
        }

        // 터치 & 드래그
        else if (Input.GetMouseButton(0))
        {
            touchEnd = Input.mousePosition;

            OnDragXY();
        }
    }

    private void OnDragXY()
    {
        // 터치 상태로 x축 드래그 범위가 dragDistance 보다 클때
        // dragDistance가 50 이상일때 movement.MoveToX 호출
        if (Mathf.Abs(touchEnd.x - touchStart.x) >= dragDistance)
        {
            movement.MoveToX((int)Mathf.Sign(touchEnd.x - touchStart.x));   // 0 또는 1 or 0 또는 -1 반환
            return;
        }

        // 터치 상태로 y축 양의 방향으로 드래그 범위가 dragDistance보다 클때
        if (touchEnd.y - touchStart.y >= dragDistance)
        {
            if (chanceGauge.jump)
            {
                movement.MoveToY();
                return;
            }
        }
    }
}
